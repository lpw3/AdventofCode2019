IntCode <- function(op) {
  i <- 1
  co <- op[1]
  while(co != 99){
    if(co == 1){
      op[op[i+3]+1] <- op[op[i+1]+1] + op[op[i+2]+1]
    }
    if(co == 2){
      op[op[i+3]+1] <- op[op[i+1]+1] * op[op[i+2]+1]
    }
    i = i+4
    co <- op[i]
  }
  return(op)
}

IntCode(op = opcode)

